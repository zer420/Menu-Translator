local bOutdated = false;

local function CheckError()
	print(string.format("[%s] Checking for outdated data...\n\tNote: Make sure that you haven't loaded any luas before, not even Menu Translator. If so please restart the game.", info.script_name));

	local bUnused, tUnused = false, {[1] = {}, [2] = {}, [3] = {}, [4] = {}, [5] = {}, [6] = {}, [7] = {},};
	for i = 1, #lang_data do
		for j = 1, #lang_data[i] do
			local name, isValid = lang_data[i][j]["en"], false;
			for k = 1, #og_ui[i] do
				if og_ui[i][k] == name and tUnused[i][k] == nil then
					tUnused[i][k] = true;
					isValid = true;
					break;
				end;
			end;
			if isValid == false then
				if bUnused == false then
					print(string.format("\n[%s] Unused data format: [line]: name\n\tNote: The position may be inaccurate for repeated control name (eg. \"Enable\").\n", info.script_name));
				end;
				print(string.format("\t[%d]: %s", GetLine(i, j) , name));
				bUnused = true;
			end;
		end;
	end;

	local bMissing, tMissing = false, {[1] = {}, [2] = {}, [3] = {}, [4] = {}, [5] = {}, [6] = {}, [7] = {},};
	for i = 1, #og_ui do
		for j = 1, #og_ui[i] do
			local name, isValid = og_ui[i][j], false;
			for k = 1, #lang_data[i] do
				if lang_data[i][k]["en"] == name and tMissing[i][k] == nil then
					tMissing[i][k] = true;
					isValid = true;
					break;
				end;
			end;
			if isValid == false then
				if bMissing == false then
					print(string.format("\n[%s] Missing data format: [level]: name\t\tbetween {control1 [line] | control2 [line]}\n\tNote: The position may be inaccurate for repeated control name (eg. \"Enable\").\n", info.script_name));
				end;
				print(string.format("\t[%d]: %s%s{%s [%d] | %s [%d]}", i, name, GetSpacing(name), og_ui[i][j - 1], GetLine(i, j - 1), og_ui[i][j + 1], GetLine(i, j + 1)));
				bMissing = true;
			end;
		end;
	end;

	bOutdated = bUnused or bMissing;

end; CheckError();

local function GetOptions(data)
    local str_opt = "{";
    for i = 1, #data[2][2] do
        str_opt = str_opt .. string.format("[%d] = \"%s\",", i, data[2][2][i]);
    end;
	str_opt = str_opt .. "},";
	return str_opt
end;

local function GenerateTable()

	if bOutdated == true then
		print(string.format("\n[%s] The %s translation has not been generated due to outdated data.", info.script_name, info.lang_name));
		return;
	end;

	local f_string = string.format("local %s = {\n\tv_loc = %.2f,\n\totdt_msg = \"%s\",\n", info.lang_name, info.lang_version, info.lang_otdt_msg);

	for i = 1, #lang_data do
		f_string = f_string .. string.format("\t[%d] = {\n", i);
		for j = 1, #lang_data[i] do

			local data = lang_data[i][j];

			if data[1] == nil then
				print("No title:",i,j)
			elseif data[2] == nil then -- name
				f_string = f_string .. string.format("\t\t[%d] = {[1] = \"%s\",},\n", j, data[1]);
			elseif data[2][1] ~= nil and data[2][2] == nil then -- name + desc
				f_string = f_string .. string.format("\t\t[%d] = {[1] = \"%s\", [2] = {[1] = \"%s\",},},\n", j, data[1], data[2][1]);
			elseif data[2][1] == nil and data[2][2] ~= nil then -- name + opt
				f_string = f_string .. string.format("\t\t[%d] = {[1] = \"%s\", [2] = {[2] = %s},},\n", j, data[1], GetOptions(data));
			elseif data[2][1] ~= nil and data[2][2] ~= nil then -- name + desc + opt
				f_string = f_string .. string.format("\t\t[%d] = {[1] = \"%s\", [2] = {[1] = \"%s\", [2] = %s},},\n", j, data[1], data[2][1], GetOptions(data)); 
			end;

		end;
		f_string = f_string .. "\t},\n";
	end;

    f_string = f_string .. string.format("};\nreturn(%s);", info.lang_name);
	file.Write(info.output_path, f_string);
	print(string.format("\n[%s] The %s translation has been successfully generated in \"%s\".", info.script_name, info.lang_name, info.output_path));

end;
GenerateTable();

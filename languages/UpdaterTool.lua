local function GetLine(i, j)
	local line = info.data_start_line + 1;
	for k = 1, i - 1 do
		line = line + #lang_data[k] + 2;
	end;
	return line + j;
end;

local function GetSpacing(name)
	return string.rep(" ",40-#name);
end;

